# flow control statements
# if-then-else conditional statement
value = 99
if value == 99:
    print("That is fast")
elif value > 200:
    print("That is too fast")
else:
    print("This is safe")
    
# for loop
for i in range(10):
    print(i)
print("End of for loop")
    
# while loop
i = 0
while i < 10:
    print(i)
    i+=1
